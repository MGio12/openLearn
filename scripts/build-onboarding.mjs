#!/usr/bin/env node
import { promises as fs } from 'fs';
import path from 'path';
import { transform } from 'esbuild';

const ROOT = path.resolve(import.meta.dirname, '..');
const SOURCE_FILES = [
  'onboarding/state.jsx',
  'onboarding/profile.jsx',
  'onboarding/screens-early.jsx',
  'onboarding/screens-mid.jsx',
  'onboarding/screens-late.jsx',
  'onboarding/app.jsx',
];
const OUT_FILE = 'onboarding/onboarding.bundle.js';

const banner = '/* Generated by npm run build:onboarding. Edit onboarding/*.jsx instead. */\n';

const sources = await Promise.all(
  SOURCE_FILES.map(async (file) => {
    const content = await fs.readFile(path.join(ROOT, file), 'utf8');
    return `\n/* ${file} */\n${content}\n`;
  }),
);

const result = await transform(sources.join('\n'), {
  loader: 'jsx',
  target: 'es2018',
  format: 'iife',
  globalName: 'ObjectifLyceeOnboardingBundle',
  jsxFactory: 'React.createElement',
  jsxFragment: 'React.Fragment',
  legalComments: 'none',
  minify: true,
  sourcemap: false,
});

await fs.writeFile(path.join(ROOT, OUT_FILE), `${banner}${result.code}\n`, 'utf8');
process.stdout.write(`Built ${OUT_FILE} from ${SOURCE_FILES.length} JSX source files.\n`);
